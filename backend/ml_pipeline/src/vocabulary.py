def build_gloss_to_handshape_map() -> dict[str, dict[str, Any]]:
    """Return the committed seed handshape prior for every gloss token."""
    missing = [token for token in GLOSS_VOCAB if token not in TOKEN_TO_PROFILE]
    if missing:
        raise ValueError(f"Missing handshape profiles for: {missing}")

    mapping: dict[str, dict[str, Any]] = {}
    for token in GLOSS_VOCAB:
        profile = copy.deepcopy(HANDSHAPE_TEMPLATES[TOKEN_TO_PROFILE[token]])
        profile["gloss"] = token
        profile["category"] = TOKEN_TO_CATEGORY[token]
        profile["token_index"] = TOKEN_TO_INDEX[token]
        if token in TOKEN_OVERRIDES:
            profile.update(copy.deepcopy(TOKEN_OVERRIDES[token]))
        mapping[token] = profile
    return mapping


def vocabulary_groups_payload() -> dict[str, Any]:
    return {
        "special_tokens": SPECIAL_TOKENS,
        "group_count": len(VOCAB_GROUPS),
        "token_count": len(GLOSS_VOCAB),
        "groups": [
            {
                "name": category,
                "count": len(tokens),
                "tokens": tokens,
            }
            for category, tokens in VOCAB_GROUPS.items()
        ],
    }


def write_vocabulary_assets(output_dir: str | pathlib.Path) -> dict[str, pathlib.Path]:
    output_path = pathlib.Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)

    paths = {
        "label_map": output_path / "label_map.json",
        "vocabulary_groups": output_path / "vocabulary_groups.json",
        "gloss_to_handshapes": output_path / "gloss_to_handshapes.json",
    }

    paths["label_map"].write_text(json.dumps(GLOSS_VOCAB, indent=2) + "\n", encoding="utf-8")
    paths["vocabulary_groups"].write_text(
        json.dumps(vocabulary_groups_payload(), indent=2) + "\n",
        encoding="utf-8",
    )
    paths["gloss_to_handshapes"].write_text(
        json.dumps(build_gloss_to_handshape_map(), indent=2) + "\n",
        encoding="utf-8",
    )
    return paths


def main() -> None:
    parser = argparse.ArgumentParser(description="Write shared NIMBUS vocabulary assets")
    parser.add_argument(
        "--output-dir",
        default=str(pathlib.Path(__file__).resolve().parent.parent / "data"),
        help="Directory where label_map.json, vocabulary_groups.json, and gloss_to_handshapes.json are written.",
    )
    args = parser.parse_args()
    write_vocabulary_assets(args.output_dir)


if __name__ == "__main__":
    main()
