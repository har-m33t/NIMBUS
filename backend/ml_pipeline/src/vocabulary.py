for token in GLOSS_VOCAB:
    if "-" in token:
        TOKEN_ALIASES.setdefault(token.replace("-", " "), token)
        TOKEN_ALIASES.setdefault(token.replace("-", ""), token)

if len(GLOSS_VOCAB) != len(set(GLOSS_VOCAB)):
    raise ValueError("Vocabulary contains duplicate tokens")
if not 100 <= len(GLOSS_VOCAB) <= 150:
    raise ValueError(f"Expected 100-150 tokens, found {len(GLOSS_VOCAB)}")


def canonicalize_gloss(raw_gloss: str) -> str:
    """Normalize external gloss labels into the committed MVP vocabulary."""
    token = raw_gloss.strip().upper()
    token = token.replace("_", "-").replace("/", "-")
    token = token.replace("’", "'").replace("`", "'")
    token = re.sub(r"\([^)]*\)", "", token)
    token = re.sub(r"\s+", "-", token)
    token = token.rstrip("+")
    token = token.strip("-")
    if not token:
        return "[UNKNOWN_SIGN]"

    alias_candidates = {
        token,
        token.replace("-", " "),
        token.replace("-", ""),
        token.replace("'", ""),
    }
    for candidate in alias_candidates:
        if candidate in TOKEN_ALIASES:
            return TOKEN_ALIASES[candidate]
        if candidate in TOKEN_TO_INDEX:
            return candidate

    return token if token in TOKEN_TO_INDEX else "[UNKNOWN_SIGN]"


